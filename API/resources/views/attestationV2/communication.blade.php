        <td class="font-label font-weight-bolder">La communication de renseignements personnels</td></tr>
       <tr><td class="font-label-small" style="">Le personnel autorisé de la Société de l’assurance automobile du Québec traite de façon confidentielle les renseignements personnels qui 
lui sont confiés. Ils sont indispensables pour appliquer les lois que la Société est chargée d’appliquer, notamment le <span class="italic-s">Code de la sécurité 
routière</span>, la <span class="italic-s">Loi sur l’assurance automobile</span> et <span class="italic-s">la Loi concernant le transport rémunéré de personnes par automobile</span>. La Société communique 
ces renseignements à ses mandataires et à certains ministères et organismes, selon <span class="italic-s">la Loi sur l’accès aux documents des organismes publics 
et sur la protection des renseignements personnels</span>. Ces renseignements peuvent aussi servir aux fins de production de statistiques, d’étude, 
de sondage, d’enquête ou de vérification. Omettre de les fournir peut entraîner un refus du service demandé. Vous pouvez consulter les 
renseignements personnels vous concernant, en obtenir une copie ou les faire corriger.</td></tr>
